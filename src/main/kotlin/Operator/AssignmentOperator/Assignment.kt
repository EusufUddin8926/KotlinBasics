package Operator.AssignmentOperator

fun main(){

    var a: Int = 4
    a +=2
    println("Value of a+=2 = ${a}")
    a -=2
    println("Value of a-=2 = ${a}")
    a *=2
    println("Value of a*=2 = ${a}")
    a /=2
    println("Value of a/=2 = ${a}")
    a %=2
    println("Value of a%=2 = ${a}")



}